        <h3>Disclosure Scotland: A Requirement for Helpers</h3>
        <p>As part of our commitment to keeping children safe, all parents/helpers must undergo a Disclosure Scotland check before they can assist with Cub Scout activities. This ensures that everyone involved in our program is properly vetted and meets the necessary safety standards. Obanshire Cub Scouts require all leaders, parents and helpers to obtain a level 2 disclosure.</p>
        
